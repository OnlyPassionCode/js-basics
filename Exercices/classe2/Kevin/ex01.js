function show0to10F(){
    for(let i = 0; i <= 10; ++i) console.log(i);
}
const show0to10FA = () => {
    for(let i = 0; i <= 10; ++i) console.log(i);
}

function show10to0F(){
    for(let i = 10; i >= 0; --i) console.log(i);
}
const show10to0FA = () => {
    for(let i = 10; i >= 0; --i) console.log(i);
}

show0to10F();
show0to10FA();
show10to0F();
